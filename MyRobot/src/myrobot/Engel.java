package myrobot;


public class Engel {
    private int x,y;
    
    public Engel(){
        x = 1;
        y = 1;
    }
    
    public Engel(int x, int y){
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
   

    
}